/* ── Skills ── */
export interface SkillLevel {
  level: number;
  description: string;
}

export interface HeroSkill {
  name:      string;
  category:  string;
  type:      'normal' | 'active' | 'global' | 'exclusive';
  levels?:   SkillLevel[];
  effect?:   string;
  stars?:    number;
}

