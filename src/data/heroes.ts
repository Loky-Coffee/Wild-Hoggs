/* ── Skills ── */
export interface SkillLevel {
  level: number;
  description: string;
}

export interface HeroSkill {
  name:      string;
  category:  string;
  type:      'normal' | 'active' | 'global' | 'exclusive';
  levels?:   SkillLevel[];
  effect?:   string;
}

/* ── Role ── */
export type HeroRole = 'attack' | 'support' | 'defense';

/* ── Hero ── */
export interface Hero {
  id:            string;
  name:          string;
  rarity:        HeroRarity;
  faction:       HeroFaction;
  role:          HeroRole;
  globalPassive: boolean;
  description:   string;
  image:         string;
  skills?:       HeroSkill[];
}

